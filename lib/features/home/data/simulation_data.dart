  biology(Icons.biotech, 'Biology', '생물'),
  quantum(Icons.blur_on, 'Quantum', '양자'),
  astronomy(Icons.rocket_launch, 'Astronomy', '천문'),
  relativity(Icons.hourglass_empty, 'Relativity', '상대성'),
  earth(Icons.public, 'Earth Science', '지구과학');

  final IconData icon;
  final String labelEn;
  final String labelKo;
  const SimCategory(this.icon, this.labelEn, this.labelKo);

  String get label => labelEn;
  String getLabel(bool isKorean) => isKorean ? labelKo : labelEn;
}

/// Simulation metadata with bilingual support
class SimulationInfo {
  final String simId;
  final SimCategory category;
  final int difficulty;
  final String title;
  final String? _titleKo;
  final String level;
  final String? _levelKo;
  final String format;
  final String? _formatKo;
  final String summary;
  final String? _summaryKo;
  final String? subcategory;
  final String? formula;

  const SimulationInfo({
    required this.simId,
    required this.category,
    required this.title,
    String? titleKo,
    required this.level,
    String? levelKo,
    required this.format,
    String? formatKo,
    required this.summary,
    String? summaryKo,
    this.difficulty = 2,
    this.subcategory,
    this.formula,
  }) : _titleKo = titleKo,
       _levelKo = levelKo,
       _formatKo = formatKo,
       _summaryKo = summaryKo;

  // Getters with fallback to English
  String get titleKo => _titleKo ?? title;
  String get levelKo => _levelKo ?? level;
  String get formatKo => _formatKo ?? format;
  String get summaryKo => _summaryKo ?? summary;

  String getTitle(bool isKorean) => isKorean ? titleKo : title;
  String getLevel(bool isKorean) => isKorean ? levelKo : level;
  String getFormat(bool isKorean) => isKorean ? formatKo : format;
  String getSummary(bool isKorean) => isKorean ? summaryKo : summary;
}

